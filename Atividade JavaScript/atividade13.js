//Criar uma variável 'nota' com valor 85
//Se nota >= 90, exibir 'Excelente'
//Se nota >= 70, exibir 'Bom'
//Se nota >= 50, exibir 'Regular'
//Senão, exibir 'Insuficiente'

let nota = 85;

if (nota >= 90) {
    console.log("Excelente");
} else if (nota >= 70) {
    console.log("Bom");
} else if (nota >= 50) {
    console.log("Regular");
} else {
    console.log("Insuficiente");
}